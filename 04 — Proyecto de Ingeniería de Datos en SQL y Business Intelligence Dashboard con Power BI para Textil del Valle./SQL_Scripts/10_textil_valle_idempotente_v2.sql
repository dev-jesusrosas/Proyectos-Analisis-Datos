use textil_db;

-- 1. Crear tabla clientes 
CREATE TABLE clientes (
	cliente_id INT AUTO_INCREMENT PRIMARY KEY,
	ruc varchar(11) UNIQUE NOT NULL,
	razon_social VARCHAR(150) NOT NULL,
	direccion VARCHAR(200),
	telefono VARCHAR(15)

);

-- 2. Crear tabla de empleados
CREATE TABLE empleados (
	empleado_id INT auto_increment PRIMARY KEY,
	dni VARCHAR(8) UNIQUE NOT NULL,
	nombre_completo varchar(150) NOT NULL,
	area VARCHAR(50) NOT NULL, --  Ej. Producción, Ventas, Logística
	cargo varchar(50)
);

-- 3. Tabla Pedidos (Aquí se conecta todo el negocio)
CREATE TABLE pedidos (
    pedido_id INT AUTO_INCREMENT PRIMARY KEY,
    cliente_id INT NOT NULL,  -- Quién compra
    empleado_id INT NOT NULL, -- Quién vende (Vendedor/Asesor)
    fecha_pedido DATE NOT NULL,
    estado VARCHAR(20) DEFAULT 'Pendiente',
    FOREIGN KEY (cliente_id) REFERENCES clientes(cliente_id) ON DELETE CASCADE,
    FOREIGN KEY (empleado_id) REFERENCES empleados(empleado_id) ON DELETE CASCADE
);

-- 4. Tabla Detalle_Pedidos (Para los productos/telas específicos)
CREATE TABLE detalle_pedidos (
    detalle_id INT AUTO_INCREMENT PRIMARY KEY,
    pedido_id INT NOT NULL,
    tipo_tela VARCHAR(100) NOT NULL, 
    cantidad INT NOT NULL,
    precio_unitario DECIMAL(10,2) NOT NULL,
    FOREIGN KEY (pedido_id) REFERENCES pedidos(pedido_id) ON DELETE CASCADE
);


